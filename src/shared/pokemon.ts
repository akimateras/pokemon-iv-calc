import type { PokemonSpecies } from "./types";

type BaseStatTuple = readonly [string, number, number, number, number, number, number];

function pokemon(
    dexNumber: number,
    name: string,
    defaultSet: BaseStatTuple,
    ...variants: BaseStatTuple[]
): PokemonSpecies {
    const toStatSet = ([label, hp, attack, defense, spAttack, spDefense, speed]: BaseStatTuple) => ({
        label,
        baseStats: { hp, attack, defense, spAttack, spDefense, speed },
    });
    const defaultStatSet = toStatSet(defaultSet);
    return {
        dexNumber,
        name,
        baseStats: defaultStatSet.baseStats,
        baseStatSets: [defaultStatSet, ...variants.map(toStatSet)],
    };
}

